using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace _4._3
{
    class Rectangle
    {
        public double length;
        public double width;
        public double s(double a, double b)
        {
            return a * b;
        }
        public double l(double x, double y)
        {
            return (x + y) * 2;
        }
    }
}
